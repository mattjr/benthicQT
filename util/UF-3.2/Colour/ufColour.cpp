void Colour_Map::decode_colour_table(std::string const& s, Colour<unsigned char> & colour )
{
    std::stringstream ss;


    std::string name;
    unsigned int r = 0;
    unsigned int g = 0;
    unsigned int b = 0;

    // Read the name.
   ss << s;
   int unwanted = ss.peek();
    while (unwanted != ',')
    {
      name.push_back((char)ss.get());
      unwanted = ss.peek();
    }
    std::string t;
    ss >> t;
    // Replace "," with " ".
    std::replace_if(t.begin(),t.end(),std::bind2nd(std::equal_to<char>(),','),' ');
    ss.clear();
    ss << t;
    ss >> r;
    ss >> g;
    ss >> b;

    colour.set_colour(name,(unsigned char)r,(unsigned char)g,(unsigned char)b,255);
}
